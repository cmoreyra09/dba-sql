/*



    Una vista en SQL Server es un objeto de base de datos que actua como una tabla virtual que contiene
    datos de una o varias tablas. No contiene ningun dato y no existe fisicamente en la base de datos de una o varias tablas.

    No contiene nigun dato y no existe fisicamente en la base de datos. De manera similar a una tabla sql, el nombre de la vista
    debe ser unico en una base de datos.

    Una vista no requiere almacenamiento en una base de datos porque no existe fisicamente.
    En una vista, tambien podemos controlar la seguridad del usuario para cceder a los datos de las tablas de la base de datos.

    Podemos permitir los usuarios obtengan los datos de la vista y el usuario no necesita permito para cada tabla o columna para obtener datos.



    La vista se define a partir de una consulta SELECT que puede extraer datos de una o mas tablas u otras vistas.
    Aunque una vista no almacena datos por si misma, proporciona una manea conveniente de encapsular y simplificar consultas complejas,
    permitiendo que los usuarios accedan a datos filtrados o combinados de una manera mas sencilla.


    En resumen, una vista es una poderosa herramienta en SQL server que permite simplificar el acceso a datos y crear abstracciones
    sobre la base de datos.

*/